package S01_FirstSteps;

public class L09_CharBool {
    public static void main(String[] args) {

        char myChar = 'K';
        /*
        * Char is single character only
        * Enclosed in single quotes as compared to strings
        * */

        char myUnicode = '\u0044';
        char myDecimal = 68;

        System.out.println(myChar);
        System.out.println(myUnicode);
        System.out.println(myDecimal);


        boolean isTrueVal = true;
        boolean isFalseVal = false;

        System.out.println(isTrueVal);
        System.out.println(isFalseVal);

    }
}
